-module(user_model, [Id, Username, Age, Sex]).
-compile(export_all).
%-export([
  %user_register/1,
  %user_login/1
%]).

%user_register({Username, Udid}) ->
  %{ok, {Username, Udid}}.

%user_login({Username, Udid}) ->
  %UserId = Udid,
  %{ok, {Username, UserId}}.

%sync_to_mem(UserId) ->
  %{ok, UserId}.

%sync_to_db(UserId) ->
  %{ok, UserId}.
